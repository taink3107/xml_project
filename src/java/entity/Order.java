package entity;

import java.sql.Date;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author taink
 */
@XmlRootElement(name = "order")
public class Order {
    private int order_id;
    private Customer customer;
    private Store store;
    private Staff staff;
    private String order_date;
    private String required_date;
    private String shipped_date;
    private int order_status;

    public int getOrder_id() {
        return order_id;
    }

    public void setOrder_id(int order_id) {
        this.order_id = order_id;
    }

    public Customer getCustomer() {
        return customer;
    }

    public void setCustomer(Customer customer) {
        this.customer = customer;
    }

    public Store getStore() {
        return store;
    }

    public void setStore(Store store) {
        this.store = store;
    }

    public Staff getStaff() {
        return staff;
    }

    public void setStaff(Staff staff) {
        this.staff = staff;
    }

    public String getOrder_date() {
        return order_date;
    }

    public void setOrder_date(String order_date) {
        this.order_date = order_date;
    }

    public String getRequired_date() {
        return required_date;
    }

    public void setRequired_date(String required_date) {
        this.required_date = required_date;
    }

    public String getShipped_date() {
        return shipped_date;
    }

    public void setShipped_date(String shipped_date) {
        this.shipped_date = shipped_date;
    }

  

    public int getOrder_status() {
        return order_status;
    }

    public void setOrder_status(int order_status) {
        this.order_status = order_status;
    }

    public Order() {
    }

    @Override
    public String toString() {
        return "Order{" + "order_id=" + order_id + ", customer=" + customer + ", store=" + store + ", staff=" + staff + ", order_date=" + order_date + ", required_date=" + required_date + ", shipped_date=" + shipped_date + ", order_status=" + order_status + '}'+"\n";
    }
    
}
